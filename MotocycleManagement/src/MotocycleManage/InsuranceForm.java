    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel RecordTable = (DefaultTableModel) jTable1.getModel();
        int selectRows = jTable1.getSelectedRow();
        try {
            int id = Integer.parseInt(RecordTable.getValueAt(selectRows, 0).toString());
            int deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete ?", "Warning", JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION) {
                pst = con.prepareStatement("DELETE FROM insurance WHERE id=?");
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Deleted");
                txtCusName.setText("");
                txtCusPhone.setText("");
                txtID.setText("");
                jDateTo.setCalendar(null);
                jDateFrom.setCalendar(null);
                
                updateTable();
            }
        } catch (Exception e) {
            System.out.print(e);
        }
        
    }//GEN-LAST:event_btDeleteActionPerformed

