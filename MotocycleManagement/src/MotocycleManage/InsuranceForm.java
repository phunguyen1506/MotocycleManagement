    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String from = sdf.format(this.jDateFrom.getDate());
        String to = sdf.format(this.jDateTo.getDate());
        try {
            
            String sql = "INSERT INTO insurance(BikeName, CustomerName, Phone, FromDate, ToDate) "
                    + "VALUES (?,?,?,?,?)";
            pst = con.prepareStatement(sql);
            
            pst.setString(1, jComboBox1.getSelectedItem().toString().trim());
            pst.setString(2, txtCusName.getText());
            pst.setString(3, txtCusPhone.getText());
            pst.setString(4, from);
            pst.setString(5, to);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Save sussess");
            updateTable();
        } catch (Exception e) {
            System.out.print(e);
        }

    }//GEN-LAST:event_btSaveActionPerformed

